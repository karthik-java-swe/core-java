package org.learjava.a2z.operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int p = 2;
        int q = 6;
        int r = 6;
        System.out.println(p < q);
        System.out.println(p <= r);
        System.out.println(p > q);
        System.out.println(p >= r);
        System.out.println(p == r);
        System.out.println(p != q);
    }
}
